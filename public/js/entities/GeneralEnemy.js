function drawPixels(context, palette, data) {
    for (let y = 0; y < 16; y++) {
        for (let x = 0; x < 16; x++) {
            const c = data[y][x];
            if (c === 0) continue;
            context.fillStyle = palette[c];
            context.fillRect(x, y, 1, 1);
        }
    }
}

function createEnemyFactory(speed, palette, walk1, walk2, dead, enemyType, labelText) {
    function drawEnemy(context) {
        const isDead = this.traits.get(Killable).dead;
        if (isDead) {
            drawPixels(context, palette, dead);
        } else {
            const frame = Math.floor(this.lifetime / 0.15) % 2;
            drawPixels(context, palette, frame === 0 ? walk1 : walk2);
        }
    }

    return function createEnemy() {
        const enemy = new Entity();
        enemy.size.set(16, 16);

        enemy.addTrait(new Physics());
        enemy.addTrait(new Solid());
        enemy.addTrait(new PendulumMove());
        enemy.addTrait(new Behavior());
        enemy.addTrait(new Killable());

        enemy.traits.get(PendulumMove).speed = speed;

        enemy.draw = drawEnemy;
        enemy.enemyType = enemyType;
        enemy.labelText = labelText;

        return enemy;
    };
}

