// Utility to pick a random item from an array
export function pickRandom(arr) {
    return arr[Math.floor(Math.random() * arr.length)];
}

// Pick a random item, avoiding the last one picked
let lastCardIndex = -1;
export function pickNextCard() {
    let index;
    do {
        index = Math.floor(Math.random() * BETWEEN_LEVEL_CARDS.length);
    } while (index === lastCardIndex && BETWEEN_LEVEL_CARDS.length > 1);
    lastCardIndex = index;
    return BETWEEN_LEVEL_CARDS[index];
}
